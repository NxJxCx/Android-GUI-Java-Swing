  private void shutdownBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_shutdownBtnActionPerformed
    java.awt.Container container = android_parent.getFrame().getContentPane();
    new javax.swing.Timer(100, new java.awt.event.ActionListener() {
      private int counter = 0;
      @Override
      public void actionPerformed(java.awt.event.ActionEvent e) {
        counter++;
        switch(counter) {
          case 1:
            apps.closeAllApps();
            ((java.awt.CardLayout)container.getLayout()).next(container);
            System.out.println("Turning Off Android...");
            break;
          case 23:
            ((javax.swing.Timer)e.getSource()).stop();
            ((java.awt.CardLayout)container.getLayout()).next(container);
            break;
        }
      }
    }).start();
  }//GEN-LAST:event_shutdownBtnActionPerformed

