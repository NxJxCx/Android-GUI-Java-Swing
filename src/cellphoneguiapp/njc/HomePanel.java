  public HomePanel(AndroidGUI androidparent) {
    android_parent = androidparent;
    initComponents();
    Timer time = new Timer(0, null);
    time.setDelay(500);
    time.addActionListener(new ActionListener() {
      private boolean withColon;
      private int batteryConsumption = 0;
      private int counter = 0;

      @Override
      public void actionPerformed(ActionEvent e) {
        withColon = !withColon;
        topTime.setText(Helper.getClock12H(withColon));
        homeTime.setText(Helper.getClock24H());
        displayName.setText(android_parent.getDisplayOwnerName());
        counter++;
        if (counter % (60 * 2) == 0) {
          batteryConsumption++;
          topBattery.setText((100 - batteryConsumption) + "%");
        }
      }
    });
    time.start();
    
    apps = new AndroidAppCollection(this);
    try {
      apps.addApp("Call");
      apps.addApp("Messages");
      apps.addApp("Contacts");
      apps.addApp("Facebook");
      apps.addApp("Gallery");
      apps.addApp("Camera");
    } catch (AndroidAppError error) {
      System.err.println(error.getMessage());
    }
      
  }
  
  public AndroidGUI getAndroidParent() {
    return android_parent;
  }
  
  public AndroidAppCollection getApps() {
    return apps;
  }
  
